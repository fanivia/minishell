#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	unsigned char	*s1;

	i = 0;
	s1 = s;
	while (i != n)
	{
		s1[i] = '\0';
		i++;
	}
}
