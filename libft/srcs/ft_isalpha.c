#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	if ((ch >= 0101 && ch <= 0132) || (ch >= 0141 && ch <= 0172))
		return (1);
	return (0);
}
