#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	long long int	n;
	long long int	neg;

	i = 0;
	n = 0;
	neg = 1;
	while (((str[i] > 8) && (str[i] <= 13)) || (str[i] == 32))
		i++;
	if ((str[i] == '-') && (str[i + 1] >= '0') && (str[i + 1] <= '9'))
	{
		neg = -1;
		i++;
	}
	if (str[i] == '+')
		i++;
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		n = n * 10 + (str[i] - 48);
		if (n < 0 && n != -2147483648)
			return (neg == -1 ? 0 : -1);
		i++;
	}
	return (n * neg);
}
